                <h2 class="blog-post-title">Hilitos y Lanitas. Empresa CÚMULO se apropia del mercado.</h2>

                <p class="blog-post-meta">28 de julio del 2015 by <a href="#">Editora: PQ.</a></p>

                <p>Loreto Fuentes, dueña de la micro empresa, "Hilitos y lanitas", nos cuenta su experiencia desde que
                    se incorporó a la plataforma CÚMULO: "Nosotras, con mi hermana, comenzamos este negocio como una
                    entretención. Cuando formamos la empresa no sabíamos bien hacia dónde queríamos ir. Necesitábamos
                    apoyo, orientación y orden. Eso no estaba en ningún lado, no sabíamos bien a quién acudir."</p>
                <hr>
                <p>Iniciar un negocio no es fácil, por más que tengas buenas ideas, sustentar los gastos operativos de
                    éste es complejo. Pero sobre todo, mantener el proyecto con vida es aún más difícil. Las redes de
                    apoyo se hacen indispensables en esta etapa y esas redes son las que ofrece CÚMULO. Loreto, comenta
                    que "sin CÚMULO hubiéramos seguidos perdidas. Esta plataforma nos ofreció la oportunidad de aprender
                    y de crecer, de encontrar proveedores responsables y de calidad. Las redes que hemos formado son de
                    un valor incalculable porque gracias a ellas nuestro negocio ha crecido y se ha mantenido
                    competitivo."</p>
                <hr>
                <p>La empresa "Hilitos y Lanitas" es una de las primeras en adherirse a CÚMULO, Loreto, su dueña, ha
                    visto crecer su inversión y ahora cuenta con cinco màquinas de cocer, tres telares y un sin número
                    de herramientas para adecuar su negocio a las necesidades de mercado. Tiene 6 trabajadoras a las que
                    ha podido contratar en la medida que se vuelven fuertes y solventes. </p>
